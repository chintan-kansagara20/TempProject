using AuthLayer.ActionFilters;
using AuthLayer.Utility;
using CrystalData.Manager.Impl;
using CrystalData.Manager.Interface;
using CrystalData.Models;
using EasyCrudLibrary.Model;
using Microsoft.AspNetCore.Http;
using Microsoft.AspNetCore.Mvc;
using Newtonsoft.Json;

namespace CrystalData.API.Controllers
{
    [ApiController]
    [FullAuthorization]
    public class TbWorkFlowStatusControlle : ControllerBase
    {
        ITbWorkFlowStatusManager _TbWorkFlowStatusManager { get; set; }

        public TbWorkFlowStatusControlle(ITbWorkFlowStatusManager TbWorkFlowStatusManager)
        {
            _TbWorkFlowStatusManager = TbWorkFlowStatusManager;
        }

        [HttpPost]
        [Route("/api/Full/TbWorkFlowStatus/Get")]
        public ActionResult Get(FullGetModel model)
        {
            try
            {
                if (model.orderBy == null) { model.orderBy = new List<OrderByModel>(); }
                if (model.filtersList == null) { model.filtersList = new List<AdvanceFilterByModel>(); }
                return Ok(_TbWorkFlowStatusManager.Get(model.page, model.itemsPerPage, model.orderBy, model.filtersList));
            }
            catch (Exception ex)
            {
                return StatusCode(500, new APIResponse(ResponseCode.ERROR, ex.Message, JsonConvert.SerializeObject(ex)));
            }
        }

        [HttpPost]
        [Route("/api/Full/TbWorkFlowStatus/Add")]
        public ActionResult Add(tbWorkFlowStatusModel model)
        {
            try
            {
                return Ok(_TbWorkFlowStatusManager.Insert(model));
            }
            catch (Exception ex)
            {
                return StatusCode(500, new APIResponse(ResponseCode.ERROR, ex.Message, JsonConvert.SerializeObject(ex)));
            }
        }
    }
}
